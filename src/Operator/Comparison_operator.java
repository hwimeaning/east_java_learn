package Operator;

public class Comparison_operator {
  public static void main(String[] args) {
    int number1 = 10;
    int number2 = 30;

    System.out.println(number1 > number2);
    System.out.println(number1 >= number2);
    System.out.println(number1 <= number2);
    System.out.println(number1 < number2);
    System.out.println(number1 == number2);
    System.out.println(number1 != number2);


  }
}
